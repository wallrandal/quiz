<template>
    <div class="component">
        <h3>You may view the User Details here</h3>
        <p>Many Details</p>
        <p>User name: {{ switchName() }}</p>
        <button @click="resetName()" >Reset name</button>
    </div>
</template>

<script>
    export default {
        props: {
            name: String,
        },
        methods: {
            switchName() {
                return this.name.split("").reverse().join("");
            },
            resetName() {
                this.name = "arara";
                this.$emit('nameWasReset', this.name);
            },
        },
    }
</script>

<style scoped>
    div {
        background-color: lightcoral;
    }
</style>